#Conversión de cadenas en números
# Se usa int para realizar la conversión en un
# número entero y float para hacerlo en un número de punto flotante o decimal.

demo_int = int('215')
print(demo_int)

demo_float = float('215.3')
print(demo_float)

# Output:
# 215
# 215.3


# Valores absolutos
# Use abs para convertir el valor negativo en su valor absoluto.
# Si hace la misma operación mediante abs (e imprime las respuestas), verá que muestra 23 para ambas ecuaciones.

print(abs(39 - 16))
print(abs(16 - 39))
# Output
# 23
# 23


#Redondeo Úsela para redondear hacia arriba al entero más cercano si el
# valor decimal es .5 o mayor, o bien hacia abajo si es menor que .5.

print(round(14.5))
# Output: 15






